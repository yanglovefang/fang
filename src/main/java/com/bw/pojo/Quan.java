package com.bw.pojo;

public class Quan {
	private int qid;
	private double wenq;//文明权重
	private double jingjiq;//经济权重
	private double huanq;//环境权重
	private double ziq;//资源权重
	private double shengq;//生活权重
	private double anquanq;//安全权重
	public int getQid() {
		return qid;
	}
	public void setQid(int qid) {
		this.qid = qid;
	}
	public double getWenq() {
		return wenq;
	}
	public void setWenq(double wenq) {
		this.wenq = wenq;
	}
	public double getJingjiq() {
		return jingjiq;
	}
	public void setJingjiq(double jingjiq) {
		this.jingjiq = jingjiq;
	}
	public double getHuanq() {
		return huanq;
	}
	public void setHuanq(double huanq) {
		this.huanq = huanq;
	}
	public double getZiq() {
		return ziq;
	}
	public void setZiq(double ziq) {
		this.ziq = ziq;
	}
	public double getShengq() {
		return shengq;
	}
	public void setShengq(double shengq) {
		this.shengq = shengq;
	}
	public double getAnquanq() {
		return anquanq;
	}
	public void setAnquanq(double anquanq) {
		this.anquanq = anquanq;
	}
	
}
